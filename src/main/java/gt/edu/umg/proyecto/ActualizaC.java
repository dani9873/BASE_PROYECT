    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed

        try {
            conexion.setAutoCommit(false);
            LoggerUtil.logInfo("Start transaction");
            PreparedStatement pst = conexion.prepareStatement("Update cliente SET nombre='"
                    + tfNombres.getText() + "',apellido='"
                    + tfApellidos1.getText() + "',no_telefono='"
                    + tfTelefono.getText() + "' WHERE id='" + tfActualizar.getText() + "'");
            int resultado = pst.executeUpdate();
            cargar("");
            if (resultado == 1) {
                JOptionPane.showMessageDialog(null, "Cliente actualizado");
                LoggerUtil.logInfo(MessageFormat.format("Se ha actualizado un cliente: {0} y monto: {1}", tfNombres, tfApellidos1));
            } else {
                JOptionPane.showMessageDialog(null, "El cliente no existe");
                LoggerUtil.logInfo("Error el cliente no existe");
            }
        } catch (Exception e) {
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(null, "Ocurrio un error");
            LoggerUtil.logInfo(MessageFormat.format("Error", e));
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

