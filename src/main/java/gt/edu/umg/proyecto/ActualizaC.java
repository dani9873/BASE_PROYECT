    void cargar(String valor) {
        String mostrar = "SELECT Id, Nombre, Apellido, No_telefono FROM cliente;";
        String[] titulos = {"Id", "Nombre", "Apellido", "No_telefono"};
        String[] Registros = new String[4];
        model = new DefaultTableModel(null, titulos);
        try {
            Statement st = conexion.createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                Registros[0] = rs.getString("Id");
                Registros[1] = rs.getString("Nombre");
                Registros[2] = rs.getString("Apellido");
                Registros[3] = rs.getString("No_telefono");
                model.addRow(Registros);
            }
            tbclientes.setModel(model);
        } catch (Exception ex) {
            Logger.getLogger(BorrarC.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

