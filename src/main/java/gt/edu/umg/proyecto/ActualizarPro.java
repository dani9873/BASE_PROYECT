    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
    try {
        conexion.setAutoCommit(false);
        LoggerUtil.logInfo("Start transaction");
        PreparedStatement pst = conexion.prepareStatement("Update proveedor SET nombre='"
                +tfCodigo.getText()+ "',apellido='"
                +tfDescripcion.getText()+ "',direccion='"
                +tfNumero.getText()+ "',no_telefono='"
                +tfPrecio.getText()+ "' WHERE Id='"+tfActualizar.getText()+"'");
        int resultado = pst.executeUpdate();
        cargar("");
        if (resultado==1){
                JOptionPane.showMessageDialog(null,"Proveedor actualizado");
                        LoggerUtil.logInfo(MessageFormat.format("Confirmado {0} y {1}", tfCodigo,tfPrecio));

            }
            else {
                JOptionPane.showMessageDialog(null,"El proveedor no existe");
                LoggerUtil.logInfo("El proveedor no existe"); 
            }
        }catch(Exception e){
            System.out.print(e.getMessage());
            JOptionPane.showMessageDialog(null,"Ocurrio un error");
                            LoggerUtil.logInfo(MessageFormat.format( "Error {0}", e));
            
        }
    
    }//GEN-LAST:event_btnactualizarActionPerformed

